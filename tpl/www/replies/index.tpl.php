<p>The following configuration options are available...</p>

<ul class="spaced">
	<li>
		Pause the service at any time.
	</li>
	<li>
		Set how often you want to get emails, from ASAP to daily.
	</li>
	<li>
		Choose whether to include or ignore your own tweets.
	</li>
	<li>
		Choose whether to include all mentions or just those that start with @your_username (replies).
	</li>
</ul>

<p>
Please note that this service is not intended to be realtime. Even if you set the frequency with the aim to get instant notifications you won't. We try to be nice to the Twitter API and limit the calls we make which can cause delays. We are continuously working on optimising the system to be as efficient as possible but please bear with us while we perfect it.
</p>

