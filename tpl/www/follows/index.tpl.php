<p>The following configuration options are available...</p>

<ul>
	<li>
		Pause the service at any time.
	</li>
	<li>
		Set how often you want to get emails; either daily, weekly or monthly.
	</li>
	<li>
		Choose to have the follower change summary emailed to you or POSTed to a URL in JSON format.
	</li>
</ul>

