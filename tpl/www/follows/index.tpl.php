<h1>Follows</h1>

<p>
	Get changes to your followers list emailed to you regularly.
	New followers and ex-followers, aggregated into a single email either daily, weekly or monthly.
</p>

<p>Getting started is really simple...</p>

<ol>
	<li>Add noreply@twitapps.com to your address book or safe senders list to ensure you get the emails.</li>
	<li><a href="/account/">Sign in to TwitApps with Twitter</a> (no password required!)</li>
	<li>Install the Follows service in your account and set configuration options.</li>
	<li>Sit back and relax.</li>
</ol>

<p>We'll keep track of your followers and send you a periodic email summarising the changes since your last email.</p>

